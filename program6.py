marks= int(input("Enter the marks of the student"))

if marks >= 90:
    print("The grade is A+")

elif marks >80:
    print("The grade is A")

elif marks >=70:
    print("The grade is B+")

elif marks >=60:
    print("The grade is B")

elif marks >=50:
    print("The grade is C")

else:
    print("Student got fail")